#define WIDTH 10
#define HEIGHT 10

static int get_ascii_fontdata(const char *code, uint8_t *mat, uint16_t *len)
{
    uint16_t i;
    uint16_t char_size = HEIGHT * (WIDTH / 8 + (WIDTH % 8 ? 1 : 0));

    uint16_t num = sizeof(Font10_Table) / sizeof(zh_cn_t);      //汉字个数
    for (i = 0; i < num; i++) {
        if ((Font10_Table[i].Index[0] == code[0]) && (Font10_Table[i].Index[1] == code[1])) {
            break;
        }
    }
    if (i == num) {
        return -1;
    }

    const uint8_t *ptr = &Font10_Table[i].Msk;
    for (i = 0; i < char_size; i++) {
        mat[i] = ptr[i];
    }
    *len = char_size;
    return 0;
}

const sFONT Font10_hz = {
    .get_fontdata = get_ascii_fontdata,
    .Width = WIDTH, /* Width */
    .Height = HEIGHT, /* Height */
    .data_size = HEIGHT * (WIDTH / 8 + (WIDTH % 8 ? 1 : 0)),
};

